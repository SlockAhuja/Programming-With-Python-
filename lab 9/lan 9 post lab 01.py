import pandas as pd

# Create two Series
s1 = pd.Series([10, 20, 30, 40])
s2 = pd.Series([2, 4, 6, 8])

# Add
print("Addition:\n", s1 + s2)

# Subtract
print("Subtraction:\n", s1 - s2)

# Multiply
print("Multiplication:\n", s1 * s2)

# Divide
print("Division:\n", s1 / s2)
